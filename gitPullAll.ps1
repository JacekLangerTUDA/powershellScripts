#list of repos relevant for vfl 
$repos = "vfl", "ms-sql-docker","latex-web-api-docker";
#find the folder where vfl resides
Set-Location $HOME;
$vfl = Get-ChildItem . -Recurse |Where-Object -Property Directory -Match "\\tuda"
##cd into the directory

#build the Path to the tuda folder
function buildPath(){
    $c="";
    foreach($s in $vfl[0].DirectoryName.Split("\\")){ 
        if($s -eq "tuda"){
        break;
        }
    $c += $s+"\";
    }
    return $c+"tuda";
}
$vflPath = buildPath;
Write-Host "
";
Set-Location $vflPath;
#loop over the repos and pull changes from the remote repo
foreach($repo in $repos){
Write-Host $repo":
------------------";
Set-Location $repo;
git pull --all;
Set-Location ..;
Write-Host "
";
}

